package com.example.capstonedesign.domain.housingannouncements.entity;

/**
 * HousingStatus
 * -----------------------------------------------------
 * 역할:
 * - LH(한국토지주택공사) 및 기타 주거 공고의 진행 상태를 나타내는 열거형(Enum)
 * - 공고 목록 및 상세 데이터 파싱 시 상태 분류 기준으로 사용됨
 */
public enum HousingStatus {

    /** 공고가 게시된 상태 (아직 접수 시작 전) */
    공고중,

    /** 신청(접수)이 진행 중인 상태 */
    접수중,

    /** 공고 내용이 정정되어 수정 공지가 올라온 상태 */
    정정공고중,

    /** 신청 접수가 마감된 상태 (선정 결과 대기 중 등) */
    접수마감,

    /** 공고가 완전히 종료된 상태 (모집 완료 또는 삭제 등) */
    종료
}
